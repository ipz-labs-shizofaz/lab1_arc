#!/bin/bash

# Назва самого скрипта
script_name=$(basename "$0")

# Перевірка, що передано два параметри
if [ $# -ne 2 ]; then
  echo "Помилка: потрібно передати два числові параметри!"
  exit 1
fi

# Присвоюємо параметри
num1=$1
num2=$2

# Порівняння
if [ "$num1" -gt "$num2" ]; then
  echo "Перший параметр ($num1) більший за другий ($num2)."
  echo "Список псевдонімів вашої системи:"
  bash -i -c alias
else
  echo "Перший параметр ($num1) не більший за другий ($num2)."
  echo "Розмір файлу скрипта '$script_name':"
  stat -c%s "$0"   # виводить розмір у байтах
fi
